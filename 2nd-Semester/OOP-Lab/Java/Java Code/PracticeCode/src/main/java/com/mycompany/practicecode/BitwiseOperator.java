
package com.mycompany.practicecode;

public class BitwiseOperator {
    public static void main(String[] args) {
        int a=5,b=9;
        System.out.println(a++);
        System.out.println(--b);
        System.out.println(a);
        System.out.println(b);
        System.out.println(a==b);
        System.out.println(a);
        System.out.println(b);
        System.out.println(a*=3);
        System.out.println(a|b);
        System.out.println(a^b);
    }
}
